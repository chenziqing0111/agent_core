def build_disease_prompt(gene: str, numbered_abstracts: str) -> str:
    return f"""
你是一个资深医学专家，正在调研基因 {gene} 与人类疾病的关系。

请阅读以下编号文献摘要（[1]~[N]），并回答：

1. 该基因涉及哪些疾病？
2. 这些疾病的发病机制是怎样的？
3. 有哪些尚未满足的临床需求？

请在引用具体信息时，用 [1]、[2] 等引用标注来标记该信息对应的摘要编号，编号顺序请参照下方摘要顺序。回答完成后无需再次列出参考文献。

请以如下结构输出：
### 疾病机制与临床需求（Gene: {gene}）
- 疾病关联：
- 发病机制：
- 临床需求：

【编号摘要对照】
{numbered_abstracts}
""".strip()

def build_treatment_prompt(gene: str, numbered_abstracts: str) -> str:
    return f"""
你是一个医学临床顾问，请围绕基因 {gene} 阅读以下编号摘要，并回答：

1. 当前与该基因相关的治疗方法有哪些？
2. 包括传统治疗、靶向药物、免疫治疗等。

请在引用具体信息时，用 [1]、[2] 等引用标注来标记该信息对应的摘要编号，编号顺序请参照下方摘要顺序。回答完成后无需再次列出参考文献。

请以如下结构输出：
### 治疗策略分析（Gene: {gene}）
- 已有治疗策略：
- 临床研究现状：
- 与该基因直接相关的干预方法：

【编号摘要对照】
{numbered_abstracts}
""".strip()

def build_target_prompt(gene: str, numbered_abstracts: str) -> str:
    return f"""
你是一名药物研发专家，请阅读以下关于基因 {gene} 的编号摘要，分析潜在治疗靶点：

1. 与该基因相关的关键通路、蛋白复合物或信号机制？
2. 有哪些潜在干预位点值得关注？
3. 研究处于何种阶段（如体外实验、动物模型、临床前、临床）？

请在引用具体信息时，用 [1]、[2] 等引用标注来标记该信息对应的摘要编号，编号顺序请参照下方摘要顺序。回答完成后无需再次列出参考文献。

请以如下结构输出：
### 靶点分析与研究进展（Gene: {gene}）
- 作用通路：
- 潜在靶点：
- 研究状态：

【编号摘要对照】
{numbered_abstracts}
""".strip()